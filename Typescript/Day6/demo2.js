//Json Object
//json=> javascript Object Notation
// it store key & value format
var obj = {
    fname: "Sumit",
    "lname": "Raokhande",
    id: 5
};
// data retrive using Dot operator
console.log("\n            First name " + obj.fname + "\n            Last name " + obj.lname + "\n            id        " + obj.id + "\n");
//data retrive using square operator/bracket
console.log("\n            First name " + obj["fname"] + "\n            Last name " + obj['lname'] + "\n            id        " + obj['id'] + "\n");
